'''3. Есть два списка
Необходимо реализовать генератор, возвращающий кортежи вида (<tutor>, <klass>), например:
('Иван', '9А')
('Анастасия', '7В')
 '''

tutors = ['Иван', 'Анастасия', 'Петр', 'Сергей', 
    'Дмитрий', 'Борис', 'Елена']
klasses = ['9А', '7В', '9Б', '9В', '8Б', '10А', '10Б', '9А']

tutkl = (el for el in zip(tutors,klasses))

for el in range(max(len(tutors),len(klasses))):
    print(next(tutkl))